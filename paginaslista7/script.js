function verSeEhMaior() {
const primeiroNumero = prompt('Digite um numero');
const segundoNumero = prompt('Digite outro numero');

if(primeiroNumero > segundoNumero)
{
    alert('O Primeiro é maior');
}
else
{
    alert('O segundo numero é maior')
}


}

